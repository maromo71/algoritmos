# Solicite três números e exiba a soma deles 
# e o produto dos três números.

# Entrada dos valores
n1 = int(input("Digite o primeiro valor: "))
n2 = int(input("Digite o segundo valor: "))
n3 = int(input("Digite o terceiro valor: "))

#soma e produto
soma = n1 + n2 + n3
produto = n1 * n2 * n3

#imprimindo os resultados
print("A soma dos valores: ", soma)
print("O produto dos valores: ", produto)